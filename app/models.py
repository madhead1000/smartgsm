from . import db
import datetime


class News(db.Model):
    __tablename__ = 'news'
    id = db.Column(db.Integer, primary_key=True)
    newsid = db.Column(db.Integer)
    Picture = db.Column(db.String(1000))
    Content = db.Column(db.String(10000))
    Name = db.Column(db.String(1000))

class Comments_news(db.Model):
    __tablename__ = 'Comments_news'
    id = db.Column(db.Integer, primary_key=True)
    newsid = db.Column(db.Integer)
    created_at = db.Column(db.DateTime, default=datetime.datetime.now())
    body = db.Column(db.String(255))
    author = db.Column(db.String(100))
    replies = db.relationship('Replies_news', backref='Comments_news',
                            lazy='subquery')

    def __init__(self,newsid,created_at,body,author):
      self.newsid = newsid
      self.created_at = created_at
      self.body = body
      self.author = author
      
class Replies_news(db.Model):
  __tablename__ = 'Replies_news'
  id = db.Column(db.Integer, primary_key=True)
  created_at = db.Column(db.DateTime, default=datetime.datetime.now())
  body = db.Column(db.String(255))
  author = db.Column(db.String(100))
  ids = db.Column(db.Integer, db.ForeignKey('Comments_news.id'))
  
  def __init__(self,created_at,body,author,ids):
    self.created_at = created_at
    self.body = body
    self.author = author
    self.ids = ids
    
